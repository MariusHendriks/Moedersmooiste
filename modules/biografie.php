        <p>Verleidelijke jongens, verknipte verhalen – Nederpop over de kop. De frisse doch stevige sound van deze band nestelt zich in je hoofd en kruipt in je hart. Bandleden Geert van Oorschot, Bram Capel, Luuk Timmermans en Koen Beerkens maken popmuziek van het kaliber dat de speakers laat knallen. De aanstekelijke zanglijnen die frontman Geert van Oorschot in huis heeft zorgen ervoor dat je je beste moves uit de kast trekt.</p>
        <p>In 2015 bracht de band hun eerste single ‘Anders dan de Rest’ uit. Deze werd in 2016 opgevolgd door hun debuut-EP ‘Mannenpraat’. Sindsdien is er veel gespeeld, gedanst en gesjanst, maar ook veel geschreven! Daarom brengen de heren in mei 2018 hun tweede EP uit, geproduceerd door Jurriaan Sielcken (o.a. Jett Rebel, Lucas Hamming, Herman van Veen).</p>
